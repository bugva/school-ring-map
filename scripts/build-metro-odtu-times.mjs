const hi = parseLines(haftaIciRaw)
const weekday_day = []
const weekday_evening = []
for (const t of hi) {
  ;(bucketDayEvening(t) === 'day' ? weekday_day : weekday_evening).push(t)
}

const j = JSON.parse(fs.readFileSync(ringsPath, 'utf8'))
const stop = j.metro.stops.find((s) => s.id === 'metro-odtu')
if (!stop) throw new Error('metro-odtu yok')
stop.name = 'ODTÜ Metro (Koru yönü)'
stop.times = {
  weekday_day,
  weekday_evening,
  weekend: parseLines(cumartesiRaw),
  weekend_sunday: parseLines(pazarRaw),
}
fs.writeFileSync(ringsPath, JSON.stringify(j, null, 2) + '\n')
console.log('OK:', weekday_day.length, 'gündüz,', weekday_evening.length, 'akşam/gece')
