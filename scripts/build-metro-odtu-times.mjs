const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ringsPath = path.join(__dirname, '../public/data/rings.json')

function parseLines(block) {
  return block
    .split('\n')
    .map((l) => l.trim())
    .filter(Boolean)
    .map((l) => l.replace(/\./g, ':'))
}

function bucketDayEvening(hhmm) {
  const [h, m] = hhmm.split(':').map(Number)
  const t = h * 60 + m
  if (t < 6 * 60 || t >= 17 * 60) return 'evening'
  return 'day'
}

